// JavaScript 语句标识符
// JavaScript 语句通常以语句标识符开头，用以标识要执行的 JavaScript 动作。
// 语句标识符是保留字，不能用作变量名（或其他任何东西）。
// 下表列出了所有 JavaScript 语句标识符：https://www.w3school.com.cn/jsref/jsref_statements.asp
// 语句	描述
// break	退出 switch 或循环。
// const	声明具有恒定值的变量。
// class	声明类。
// continue	如果出现指定条件，则（在循环中）中断一次循环，并继续循环中的下一次迭代。
// debugger	停止执行 JavaScript，并调用调试功能（如果有）。
// do ... while	执行一个语句块，并在条件为 true 时重复该语句块。
// for	循环遍历代码块多次.
// for ... in	循环遍历对象的属性。(&&&)
// for ... of	循环遍历可迭代对象的值。(&&&)
// function	声明函数。
// if ... else ... else if	标记根据条件执行的语句块。
// let	声明在方括号 {} 作用域内的变量。
// return	停止执行函数并从该函数返回值。
// switch	根据不同情况标记要执行的语句块。
// throw	抛出（生成）错误。
// try ... catch ... finally	标记在 try 块中发生错误时要执行的语句块，并实现错误处理。
// var	声明变量。
// while	标记条件为 true 时要执行的语句块。
